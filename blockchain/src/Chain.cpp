bool Chain::addToPending(Tx *tx)
{
  int size = m_transactions.size();
  tx->setIndex(size + 1);

  decltype(m_transactions)::iterator it = m_transactions.begin();
  for (; it != m_transactions.end(); ++it)
  {
    if (strcmp(tx->getHash(), (*it)->getHash()) == 0)
    {
      return false;
    }
  }

  m_transactions.push_back(tx);

  return true;
};

