#include <string.h>
#include <blockchain/SHA256.hpp>
#include <string>
#include <sstream>

////////////////////////////////////////////////////////////////////////////////
//
////////////////////////////////////////////////////////////////////////////////
char *Tx::getHash()
{
  std::stringstream ss;

  ss << m_index
     << m_from
     << m_to
     << m_amount
     << m_timestamp;

  std::string s = ss.str();

  unsigned char dig[32];
  ::crypto::SHA256(dig, s.c_str(), s.size());

  BIGNUM *bn = BN_new();
  BN_bin2bn(dig, 32, bn);

  char *hash = BN_bn2hex(bn);

  BN_free(bn);

  return hash;
};

